CREATE OR REPLACE FUNCTION "public"."authorize"("requested_permission" "public"."app_permission") RETURNS boolean
    LANGUAGE "plpgsql" STABLE SECURITY DEFINER
    SET "search_path" TO ''
    AS $$
DECLARE
  bind_permissions INT;
  user_role public.app_role;
BEGIN
  -- Benutzerrolle aus JWT abrufen
  SELECT (auth.jwt() ->> 'user_role')::public.app_role INTO user_role;

  -- Superadmins haben automatisch alle Berechtigungen
  IF user_role = 'postgres' THEN
    RETURN TRUE;
  END IF;

  -- Für andere Rollen, Berechtigungen zählen
  SELECT COUNT(*)
  INTO bind_permissions
  FROM public.role_permissions
  WHERE role_permissions.permission = requested_permission
  AND role_permissions.role = user_role;

  RETURN bind_permissions > 0;
END;
$$;


