CREATE OR REPLACE FUNCTION "public"."match_books"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer) RETURNS TABLE("id" "uuid", "title" "text", "author" "text", "subject" "text", "level" "text", "description" "text", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    books.id,
    books.title,
    books.author,
    books.subject,
    books.level,
    books.description,
    1 - (books.embedding <=> query_embedding) AS similarity
  FROM books
  WHERE 1 - (books.embedding <=> query_embedding) > match_threshold
  ORDER BY books.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;


