CREATE OR REPLACE FUNCTION "public"."prevent_non_available_updates"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
begin
  -- Prüfen, ob nur die Spalte 'available' geändert wird
  IF NOT (tg_op = 'UPDATE' AND tg_name = 'prevent_non_available_updates') THEN
    RETURN NEW;
  END IF;

  -- Prüfen, ob außer 'available' andere Spalten geändert wurden
  IF (row_to_json(NEW)->>'available') IS DISTINCT FROM (row_to_json(OLD)->>'available') AND
     (row_to_json(NEW)->>'title') IS DISTINCT FROM (row_to_json(OLD)->>'title') THEN
    RAISE EXCEPTION 'User can only update the available status';
  END IF;

  RETURN NEW;
end;
$$;


ALTER FUNCTION "public"."prevent_non_available_updates"() OWNER TO "postgres";


