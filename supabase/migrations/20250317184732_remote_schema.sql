CREATE OR REPLACE FUNCTION "public"."reset_embedding_on_update"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  -- Prüfen, ob relevante Felder geändert wurden
  IF 
    NEW.title IS DISTINCT FROM OLD.title OR
    NEW.author IS DISTINCT FROM OLD.author OR
    NEW.subject IS DISTINCT FROM OLD.subject OR
    NEW.level IS DISTINCT FROM OLD.level OR
    NEW.year IS DISTINCT FROM OLD.year OR
    NEW.type IS DISTINCT FROM OLD.type OR
    NEW.publisher IS DISTINCT FROM OLD.publisher OR
    NEW.description IS DISTINCT FROM OLD.description
  THEN
    -- WICHTIG: Erst vector_source aktualisieren, dann embedding auf NULL setzen
    NEW.vector_source = concat_ws(' ', 
      NEW.title, 
      NEW.author, 
      NEW.subject, 
      NEW.level, 
      NEW.type,
      NEW.publisher,  -- Publisher hinzugefügt
      NEW.year,       -- Year hinzugefügt
      NEW.description
    );
    
    -- Embedding auf NULL setzen, um Neuberechnung zu erzwingen
    NEW.embedding = NULL;
    
    -- Optional: Loggen der Änderung
    RAISE NOTICE 'Vector_source und Embedding für Buch % aktualisiert aufgrund von Feldänderungen', NEW.id;
  END IF;
  
  RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."reset_embedding_on_update"() OWNER TO "postgres";


